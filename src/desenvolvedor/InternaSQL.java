    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sql = jTextArea1.getText();
        Pattern pattern = Pattern.compile("([^;])+");
        Matcher matcher = pattern.matcher(sql);
        try {
            Firebird fb = new Firebird();
            fb.connect();
            
            while(matcher.find()) {
                PreparedStatement pstm = Firebird.conn.prepareStatement(sql.substring(matcher.start(), matcher.end()));
                pstm.execute();
            }
            
            Firebird.conn.commit();
            fb.disconnect();
            JOptionPane.showMessageDialog(this, "SQL aplicado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            new TelaErro(1, ex.getStackTrace()).setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


