    public class AtualizarPorcentagem extends Thread {
        @Override
        public void run() {
            int i;
            for(i = 1; i <= 100; i++) {
                try {
                    sleep(100);
                } catch (InterruptedException ex) {
                    new TelaErro(4, ex.getStackTrace()).setVisible(true);
                }
                porcentagem = i;
            }
        }
    }
    
    public class Temporizador extends Thread {
        @Override
        public void run() {
            try {
                while(tProgressBar.getValue() < 100) {
                    sleep(1);
                    tProgressBar.setValue(porcentagem);
                }
                //sleep(2000);
                //dispose();
            } catch (InterruptedException ex) {
                new TelaErro(4, ex.getStackTrace()).setVisible(true);
            }
        }
    }

