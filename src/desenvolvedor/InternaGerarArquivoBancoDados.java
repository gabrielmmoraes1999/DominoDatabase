    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser fc = new JFileChooser();
        fc.setDialogTitle("Selecionar pasta");
        fc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int retorno = fc.showOpenDialog(this);
        try {
            if(retorno == JFileChooser.APPROVE_OPTION) {
                File f = fc.getSelectedFile();
                jTextField1.setText(f.getPath());
                if(f.exists()) {
                    jTextArea1.setText(Sistema.decrypt(Files.readAllBytes(Paths.get(f.getAbsolutePath()))));
                }
            }
        } catch (IOException ex) {
            new TelaErro(3, ex.getStackTrace()).setVisible(true);
        } catch (Exception ex) {
            new TelaErro(1, ex.getStackTrace()).setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            try (FileOutputStream fos = new FileOutputStream(new File(jTextField1.getText()))) {
                fos.write(Sistema.encrypt(jTextArea1.getText()));
                fos.flush();
            }
            
            JOptionPane.showMessageDialog(this, "Arquivo criado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            new TelaErro(1, ex.getStackTrace()).setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


